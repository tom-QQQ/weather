package com.coolweather.app.activity;

import com.coolweather.app.R;
import com.coolweather.app.service.AutoUpdateService;
import com.coolweather.app.util.HttpCallbackListener;
import com.coolweather.app.util.HttpUtil;
import com.coolweather.app.util.Utility;

import android.app.Activity;
import android.content.Intent;
import android.content.SharedPreferences;
import android.os.Bundle;
import android.preference.PreferenceManager;
import android.text.TextUtils;
import android.util.Log;
import android.view.Menu;
import android.view.MenuItem;
import android.view.View;
import android.view.View.OnClickListener;
import android.view.Window;
import android.widget.Button;
import android.widget.LinearLayout;
import android.widget.TextView;

public class WeatherActivity extends Activity implements OnClickListener{

	private LinearLayout weatherInfoLayout;
	
	/**
	 * 用于显示城市名
	 */
	private TextView cityNameText;
	
	/**
	 * 用于显示同步状态或发布时间
	 */
	private TextView publishText;
	
	/**
	 * 用于显示天气描述信息
	 */
	private TextView weatherDespText;
	
	/**
	 * 用于显示气温1
	 */
	private TextView temp1Text;
	
	/**
	 * 用于显示气温2
	 */
	private TextView temp2Text;
	
	/**
	 * 用于显示当前日期
	 */
	private TextView currentDateText;
	
	/**
	 * 切换城市按钮
	 */
	private Button switchCity;
	
	/**
	 * 更新天气按钮
	 */
	private Button refreshWeather;
	
	protected static int onlyOne = 0;
	
	@Override
	public void onCreate(Bundle savedInstanceState) {
		super.onCreate(savedInstanceState);
		requestWindowFeature(Window.FEATURE_NO_TITLE);
		setContentView(R.layout.weather_layout);
				
		//初始化各种条件
		weatherInfoLayout = (LinearLayout) findViewById(R.id.weather_info_layout);
		cityNameText = (TextView) findViewById(R.id.city_name);
		publishText = (TextView) findViewById(R.id.publish_text);
		weatherDespText = (TextView) findViewById(R.id.weather_desp);
		temp1Text = (TextView) findViewById(R.id.temp1);
		temp2Text = (TextView) findViewById(R.id.temp2);
		currentDateText = (TextView) findViewById(R.id.current_date);
		
		switchCity = (Button) findViewById(R.id.switch_city);
		refreshWeather = (Button) findViewById(R.id.refresh_weather);
		switchCity.setOnClickListener(this);
		refreshWeather.setOnClickListener(this);
		
		//获取ChooseAreaActivity用Intent传递过来的县级天气代码
		String countyCode = getIntent().getStringExtra("county_code");
		//只要countyCode不为null或""就返回真
		if (!TextUtils.isEmpty(countyCode)) {
			//有县级代号时就去查询天气
			String synchronizeing = (String)this.getResources().
					getString(R.string.syncing);
			publishText.setText(synchronizeing);
			//暂时隐藏显示天气信息的布局
			weatherInfoLayout.setVisibility(View.INVISIBLE);
			//暂时隐藏城市名
			cityNameText.setVisibility(View.INVISIBLE);
			queryWeatherCode(countyCode);
		} else {
			//没有县级代号时就直接显示本地天气
			showWeather();
		}
	}
	
	/**
	 * 创建菜单
	 */
	@Override
	public boolean onCreateOptionsMenu(Menu menu) {
		getMenuInflater().inflate(R.menu.main, menu);
		return true;
	}
	
	/**
	 * 定义菜单相应事件
	 */
	public boolean onOptionsItemSelected(MenuItem item) {
		switch (item.getItemId()) {
		//跳转至天气更新频率活动
		case R.id.action_settings:
			Intent intent = new Intent(this, UpdateFrequencyActivity.class);
			startActivity(intent);
		}
		return true;
	}
	
	@Override
	public void onClick(View v) {
		switch (v.getId()) {
		
		case R.id.switch_city:
			Intent intent = new Intent(this, ChooseAreaActivity.class);
			intent.putExtra("from_weather_activity", true);
			startActivity(intent);
			finish();
			break;
			
		case R.id.refresh_weather:
			String syncing = (String) this.getResources().getString(R.string.syncing);
			publishText.setText(syncing);
			SharedPreferences prefs = PreferenceManager.
					getDefaultSharedPreferences(this);
			//从SharedPreferences文件中读取天气代号
			String weatherCode = prefs.getString("weather_code", "");
			//读取的代号不为空的话，重新查询天气
			if (!TextUtils.isEmpty(weatherCode)) {
				queryWeatherInfo(weatherCode);
			}
			break;
			
		default:
			break;
		}
	}

	/**
	 * 查询县级代号所所对应的天气代号
	 */
	private void queryWeatherCode(String countyCode) {
		String address = "http://www.weather.com.cn/data/list3/city" + countyCode + 
				".xml";
		queryFromServer(address, "countyCode");
	}

	/**
	 * 查询天气代号所对应的天气,接口为中国天气网,返回数据格式为json
	 */
	protected void queryWeatherInfo(String weatherCode) {
		String address = "http://www.weather.com.cn/data/cityinfo/" + weatherCode + 
				".html";
		queryFromServer(address, "weatherCode");
	}
	
	/**
	 * 根据传入的地址和类型去向服务器查询天气代号或者天气信息
	 */
	private void queryFromServer(final String address, final String type) {
		HttpUtil.sendHttpRequest(address, type, new HttpCallbackListener() {

			@Override
			public void onFinish(String response) {
				if ("countyCode".equals(type)) {
					if (!TextUtils.isEmpty(response)) {
						//从服务器返回的数据中解析出天气代号,注意是反斜杠
						String[] array = response.split("\\|");
						if (array != null && array.length == 2) {
							String weatherCode = array[1];
							queryWeatherInfo(weatherCode);
						}
					}
				} else if ("weatherCode".equals(type)) {
					//处理服务器返回的天气信息
					Utility.handleWeatherResponse(WeatherActivity.this, response);
					//涉及UI操作在主线程中进行
					runOnUiThread(new Runnable() {
						@Override
						public void run() {
							showWeather();
						}						
					}); 
				}
			}
			@Override
			public void onError(final Exception e) {
				runOnUiThread(new Runnable () {
					@Override
					public void run() {
						e.printStackTrace();
						String lost = (String) WeatherActivity.this.getResources().
								getString(R.string.sync_lost);
						publishText.setText(lost);
					}					
				});
			}			
		});
	}

	/**
	 * 从SharedPreferences文件中读取存储的天气信息，并显示到界面上,并启动后台更新天气的服务
	 */
	private void showWeather() {
		SharedPreferences prefs = PreferenceManager.getDefaultSharedPreferences(this);
		cityNameText.setText(prefs.getString("city_name", ""));
		temp1Text.setText(prefs.getString("temp1", ""));
		temp2Text.setText(prefs.getString("temp2", ""));
		weatherDespText.setText(prefs.getString("weather_desp", ""));
		publishText.setText(prefs.getString("publish_time", ""));
		currentDateText.setText(prefs.getString("current_date", ""));
		//读取成功数据后再显示隐藏的布局和view
		weatherInfoLayout.setVisibility(View.VISIBLE);
		cityNameText.setVisibility(View.VISIBLE);
		
		Intent intent = new Intent(this, AutoUpdateService.class);
		startService(intent);
	}
}
